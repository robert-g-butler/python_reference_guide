


def getNthFib(n):
    '''Returns the nth Fibonacci number.'''
    if n == 0:
        return 1
    elif n == 1:
        return 1
    print(n, '-->', getNthFib(n-2), '+', getNthFib(n-1))
    return getNthFib(n-2) + getNthFib(n-1)

getNthFib(5)






